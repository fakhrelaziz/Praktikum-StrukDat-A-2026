#TIPE DATA DALAM BAHASA PYTHON

"""
Text Type:	str
Numeric Types:	int, float, complex
Sequence Types:	list, tuple, range
Mapping Type:	dict
Set Types:	set, frozenset
Boolean Type:	bool
Binary Types:	bytes, bytearray, memoryview
None Type:	NoneType

"""

x = "Assalamualaikum Dunia"	#str	
x = 20	#int	
x = 20.5	#float	
x = 1j	#complex : ini adalah tipe data bilangan imaginer atau terdiri dari pasangan angka riil dan imajiner
x = ["keju", "susu", "mentega"]	#list	
x = ("apple", "banana", "cherry")	#tuple	
x = range(6)	#range	
x = {"name" : "Rel", "age" : 20}	#dict	
x = {"apple", "banana", "cherry"}	#set	
x = frozenset({"apple", "banana", "cherry"})	#frozenset	
x = True	#bool	
x = b"Hiiiii"	#bytes	
x = bytearray(5)	#bytearray	
x = memoryview(bytes(5))	#memoryview	
x = None	#NoneType